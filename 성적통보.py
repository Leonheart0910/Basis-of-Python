pyo = '''
----------------------
%s 학생에게
이번 2019년 %s 과목의 성적은
%s 임을 통보해 드립니다.
-------------------------'''
mem1 = ("양준석", "컴퓨터시스템입문", "A+")
mem2 = ("박인욱", "공학미적분학", "A0")
mem3 = ("이상명","기초수학", "B0")
mem4 = ("강경찬", "재글경", "C+")
mem5 = ("김종의", "일반물리학","F")

ml = [mem1, mem2, mem3, mem4, mem5]
for w in ml:
    letter = (pyo % w)
    if w[2] in ['F', 'W']:
        letter = letter + "\n 공부안하면 디진다."
    print(letter)

